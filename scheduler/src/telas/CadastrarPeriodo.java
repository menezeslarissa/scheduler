    private void salvarCadastro() {
        String periodoStr = cbAno.getSelectedItem().toString().trim() + '.' + cbSemestre.getSelectedItem().toString();
        Periodo p = new Periodo();
        p.setPeriodo(periodoStr);
   
        if(!this.controlePeriodo.salvar(p))
        {
           JOptionPane.showMessageDialog(this, "Inclus√£o cancelada. Per√≠odo selecionado j√° cadastrado.", "Scheduler",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Per√≠odo cadastrado com sucesso.", "Scheduler",JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }  
    }

