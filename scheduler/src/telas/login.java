    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        
        if (edUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe o usuário.", "Scheduler", JOptionPane.INFORMATION_MESSAGE);
            edUsuario.requestFocus();
        }
        else
        {
            if (edSenha.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Informe a senha.", "Scheduler", JOptionPane.INFORMATION_MESSAGE);
                edSenha.requestFocus();
            }
            else
            {
                try
                {
                    controlePeriodo.setPeriodo(controlePeriodo.buscarUltimoPeriodo());
                    controleLogin.setLogin(controleLogin.buscarLogin(edUsuario.getText(), edSenha.getText()));
                    new Menu(this, controleLogin,controlePeriodo).setVisible(true);
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, "Usuário e/ou senha incorreta.", "Scheduler", JOptionPane.INFORMATION_MESSAGE);
                    edUsuario.setFocusable(true);
                }    
            }
            
        }
    }//GEN-LAST:event_btEntrarActionPerformed

